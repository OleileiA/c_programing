#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>

// C语言中的基本数据类型包括整型、浮点型、字符型和布尔型。下面是各种基本数据类型的介绍和示例代码：
// 整型
// 整型是用于存储整数的数据类型。在C语言中，整型的大小和范围取决于具体实现。通常，整型被分为四种类型：short、int、long和long long。
// short：短整型，一般占用2个字节的存储空间，用于存储较小的整数。其取值范围是-32768 ~ 32767。
// int：整型，一般占用4个字节的存储空间，用于存储普通的整数。其取值范围是-2147483648 ~ 2147483647。
// long：长整型，一般占用4个字节或8个字节的存储空间，用于存储较大的整数。其取值范围是-2147483648 ~ 2147483647 或 -9223372036854775808 ~ 9223372036854775807。
// long long：长长整型，一般占用8个字节的存储空间，用于存储超大的整数。其取值范围是-9223372036854775808 ~ 9223372036854775807。
// 下面是整型的示例代码：

int main() {
    short a = 32767;
    int b = 2147483647;
    long c = 2147483647;
    long long d = 9223372036854775807;
    printf("short %d\n", a);
    printf("int %d\n", b);
    printf("long %ld\n", c);
    printf("long long %lld\n", d);
    return 0;
}

//### 浮点型
//        浮点型是用于存储浮点数的数据类型。在C语言中，浮点型有两种类型：float和double。
//- float：单精度浮点型，一般占用4个字节的存储空间，用于存储普通的浮点数。其有效位数为6~7位。
//- double：双精度浮点型，一般占用8个字节的存储空间，用于存储超大或超精确的浮点数。其有效位数为15~16位。
//下面是浮点型的示例代码：

int main2() {
    float a = 3.14159;
    double b = 3.141592653589793238462643383;
    printf("float %f\n", a);
    printf("double %lf\n", b);
    return 0;
}

//
// 字符型
// 字符型是用于存储单个字符的数据类型。在C语言中，字符型使用单引号表示。它只能存储一个字符，但也可以存储特殊字符，如换行符、制表符等等。
// 下面是字符型的示例代码：
int main3() {
    char a = 'A';
    char b = 'B';
    printf("%c%c\n", a, b);
    return 0;
}

// 布尔型
// 布尔型是用于存储真假值的数据类型。在C语言中，布尔型使用逻辑运算符返回true或false。其中，0表示false，非零表示true。
// 下面是布尔型的示例代码：
int main4() {
    bool a = true;
    bool b = false;
    printf("%d %d\n", a, b);
    return 0;
}